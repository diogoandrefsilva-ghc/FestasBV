-- GRANTs (sem isto: HTTP 403 / 42501)
GRANT ALL ON TABLE festasbv.categorias        TO anon, authenticated;
GRANT ALL ON TABLE festasbv.artigo_categorias TO anon, authenticated;

-- RLS
ALTER TABLE festasbv.categorias        ENABLE ROW LEVEL SECURITY;
ALTER TABLE festasbv.artigo_categorias ENABLE ROW LEVEL SECURITY;

-- categorias: todos os membros leem; só o admin gere o dicionário
DROP POLICY IF EXISTS categorias_sel   ON festasbv.categorias;
DROP POLICY IF EXISTS categorias_admin ON festasbv.categorias;

CREATE POLICY categorias_sel ON festasbv.categorias
  FOR SELECT TO authenticated
  USING (festasbv.is_allowed());

CREATE POLICY categorias_admin ON festasbv.categorias
  FOR ALL TO authenticated
  USING (festasbv.is_admin())
  WITH CHECK (festasbv.is_admin());

-- artigo_categorias: todos leem e podem PREENCHER buracos (é assim que a AI
-- grava sugestões quando qualquer membro importa uma fatura, e que um membro
-- categoriza um artigo novo ao criá-lo). MUDAR/limpar uma associação que já
-- existe é só do admin — a app respeita e o RLS garante.
DROP POLICY IF EXISTS artigo_categorias_sel   ON festasbv.artigo_categorias;
DROP POLICY IF EXISTS artigo_categorias_ins   ON festasbv.artigo_categorias;
DROP POLICY IF EXISTS artigo_categorias_admin ON festasbv.artigo_categorias;

CREATE POLICY artigo_categorias_sel ON festasbv.artigo_categorias
  FOR SELECT TO authenticated
  USING (festasbv.is_allowed());

CREATE POLICY artigo_categorias_ins ON festasbv.artigo_categorias
  FOR INSERT TO authenticated
  WITH CHECK (festasbv.is_allowed());

CREATE POLICY artigo_categorias_admin ON festasbv.artigo_categorias
  FOR ALL TO authenticated
  USING (festasbv.is_admin())
  WITH CHECK (festasbv.is_admin());
