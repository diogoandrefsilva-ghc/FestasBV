-- =====================================================================
-- FestasBV — Migração: Categorias de artigos (festasbv.categorias +
-- festasbv.artigo_categorias)
-- Correr no SQL Editor do Supabase (projeto festasbv).
-- É IDEMPOTENTE: pode ser corrido mais que uma vez sem erro.
-- Depende de: functions.sql (is_allowed/is_admin).
-- Ordem: schema.sql -> functions.sql -> policies.sql -> shoplist.sql ->
--        stock.sql -> ESTE.
--
-- O QUE É: tipologias de produto ("Sumos", "Talho", …) que agrupam artigos
-- diferentes da mesma família no separador 🧺 Stock e na Lista de Compras.
-- São duas tabelas GLOBAIS (sem evento_id — um produto é o mesmo em todos
-- os anos, a memória transita de festa para festa):
--
--   categorias         o dicionário (nome + descritivo). O DESCRITIVO é
--                      importante: segue no prompt do Gemini (fatura-ocr)
--                      para a AI categorizar bem os artigos novos — quanto
--                      melhor o descritivo, melhores as sugestões. Gerido
--                      pelo admin em Definições › Categorias de Artigos.
--
--   artigo_categorias  a memória artigo→categoria. A chave é o nome
--                      NORMALIZADO (minúsculas, sem acentos — o shopArtKey
--                      da app), por isso "Coca-Cola" e "coca-cola" são o
--                      mesmo artigo. origem='ai' marca o que a AI preencheu;
--                      'manual' o que foi definido/corrigido por pessoas.
--                      A AI só escreve com resolution=ignore-duplicates,
--                      logo NUNCA pisa o que já está definido.
--
-- Sem esta migração a app funciona à mesma: os fetch são tolerantes
-- (CATS_TABLE=false) e tudo o que é categorias fica simplesmente escondido.
-- =====================================================================

CREATE TABLE IF NOT EXISTS festasbv.categorias (
  id          bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  nome        text NOT NULL UNIQUE,
  descritivo  text NOT NULL DEFAULT '',   -- exemplos/âmbito; vai no prompt da AI
  criado_em   timestamptz NOT NULL DEFAULT now()
);

CREATE TABLE IF NOT EXISTS festasbv.artigo_categorias (
  artigo_key    text PRIMARY KEY,          -- shopArtKey(artigo): minúsculas, sem acentos
  categoria_id  bigint NOT NULL REFERENCES festasbv.categorias(id) ON DELETE CASCADE,
  origem        text NOT NULL DEFAULT 'manual',   -- 'manual' | 'ai'
  atualizado_em timestamptz NOT NULL DEFAULT now()
);

